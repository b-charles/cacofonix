	sol:'.':C sol:'.':C sol:D sol:D S:D sol:D S:D sol:C' S:C sol:C bar ];

PONT2 = [ Note('') ...
	~fa:C S:C +do:C ~si:C S:C sol:C S:C la:C bar ...
	~la:N +do:C +do:C ~si:C si:C sol:C ~mi:C bar ...
	~mi:N +do:C ~si:C S:C sol:C S:C la:C bar ...
	~la:N +mi:C +mi:'.':C +mi:C +mi:C ~+re:C ~+do:C bar ...
	S:N +do:C ~si:C S:C sol:C S:C la:C bar ...
	~la:N +do:C +do:C ~si:C si:C sol:C ~mi:C bar ...
	~mi:N +do:C ~si:C S:C sol:C S:C la:C bar ...
