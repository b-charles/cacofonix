%% CHANT

HOHOHO = [ Note('+') ...
	~[ do:C re:C mi:C do:C bar ...
	fa:N' mi:C fa:C mi:C re:N bar ...
	re:N ] S:C ~[ -si:C do:N re:C ] S:C bar ...
	mi:N ~mi:'.':C mi:'.':C ~[ mi:C re:N do:C bar ...
	do:N ] S:N ];

HAHA = [ Note('+') ...
	~-la:C S:C -la:N mi:C mi:'.':C ~[ fa:C mi:C ] bar ...
	S:C -la:'.':C -la:N mi:'.':C mi:'.':C ~[ fa:C mi:C ] bar ...
	-la:C S:C -la:N mi:C mi:'.':C ~[ fa:C mi:C ] bar ...
	S:C do:'.':C ~[ do:C -la:C do:C ] S:C do:N bar ];

COUPLET = [ Note('') ...
	S:C sol:'.':C la:C ~sol:C la:C la:N la:'.':C bar ...
	+do:C ~la:C sol:C la:C ~sol:N' S:C bar ...
	S:C la:'.':C la:C ~sol:'.':C la:C ~sol:C la:C ~sol:C bar ...
	+do:C ~la:C sol:C ~la:N ~mi:C sol:C ~la:C bar ...
	la:[B C] S:C S:N bar ...
	+mi:'.':N +do:'.':N la:C ~mi:'.':C sol:C ~la:C bar ...
	la:[B C] S:C S:N bar ...
	S:R bar ...
	S:C +do:'.':C +do:C ~la:C +do:C +do:N la:'.':C bar ...
	+do:C +do:'.':C la:C +do:N S:[C N] bar ...
	S:C +do:'.':C +do:C ~la:'.':C +do:C ~la:'.':C +do:C ~la:'.':C bar ...
	+do:C ~la:C la:C la:C ~sol:C ~mi:'.':C sol:C ~la:C bar ...
	la:[B C] S:C S:N bar ...
	+mi:'.':N +do:'.':N la:C ~mi:'.':C sol:C ~la:C bar ...
	la:[B C] S:C S:N bar ...
	S:R bar ...
	S:C' [sol sol sol sol fa S mi ~mi S ].*D S:N bar ...
	S:C [ sol sol sol sol sol fa S mi ~mi S ].*D S:N bar ...
	S:C la:'.':C la:C ~sol:'.':C la:N S:C sol:'.':C bar ...
	la:N ~sol:C ~-la:N' S:N bar ];

REFRAIN = [ Note('+') ...
	S:C la:'.':C la:C la:'.':C la:C la:N sol:C bar ...
	sol:C sol:'.':C sol:C ~re:C ~re:'.':C re:C ~re:'.':C mi:C bar ...
	~mi:'.':C mi:'.':C mi:C mi:'.':C sol:N re:C ~mi:D ~re:D bar ...
	do:B S:B bar ...
	S:C la:'.':C la:C la:'.':C la:C la:C la:C sol:C bar ...
	sol:C sol:'.':C sol:C ~re:C ~re:'.':C re:C ~re:'.':C mi:C bar ...
	~mi:'.':C mi:'.':C mi:C mi:'.':C laB:N re:C ~mi:D ~re:D bar ...
	do:B ];

PONT1 = [ Note('') ...
	sol:N sol:N sol:'.':C sol:'.':C sol:C sol:'.':C bar ...
	sol:'.':C sol:'.':C sol:'.':C sol:'.':C sol:'.':C sol:'.':D sol:D ~sol:C sol:'.':C bar ...
	sol:N sol:N sol:'.':C sol:'.':C sol:C sol:'.':C bar ...
	sol:'.':C sol:'.':C sol:'.':C sol:'.':C sol:'.':C sol:'.':D sol:D ~sol:C sol:'.':C bar ...
	sol:N sol:N sol:'.':C sol:'.':C sol:C sol:'.':C bar ...
	sol:'.':C sol:'.':C sol:'.':C sol:'.':C sol:'.':C sol:'.':D sol:D ~sol:C sol:'.':C bar ...
