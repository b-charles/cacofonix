N32 = Note(1/8);

delay = N8; val = { 'b', '#' };
tremolo = Note( 'pitch', val{1}, delay);
for t = 1:20
	delay = delay+N32;
	tremolo = [ tremolo ...
		Note( 'pitch', 'quad', val{1+mod(t,2)}, delay) ];
end

pitch = @(varargin) Note('pitch',varargin{:});

